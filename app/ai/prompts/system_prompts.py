def get_rag_prompt_with_context(
    query: str,
    context: str,
    user_profile: dict = None
) -> str:
    """
    构建带上下文的RAG Prompt
    
    Args:
        query: 用户问题
        context: 检索到的知识内容
        user_profile: 用户画像
    
    Returns:
        完整的Prompt
    """
    user_info = ""
    if user_profile:
        user_info = f"""
## 学习者信息
- 背景：{user_profile.get('occupation', '未知')}
- 技术水平：{user_profile.get('current_level', {})}
- 学习目标：{user_profile.get('learning_goal', '未知')}

请根据学习者的背景调整回答的深度和风格。
"""
    
    prompt = f"""{RAG_QA_SYSTEM_PROMPT}

{user_info}

## 课程知识内容
{context}

## 学习者问题
{query}

请基于上述课程知识内容，回答学习者的问题。记住要引用知识来源。
"""
    
    return prompt
